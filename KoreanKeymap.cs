using System.Collections.Generic;

namespace KoreanComposer
{
    /// <summary>
    /// 두벌식 표준 자판 (KS X 5002) 키맵.
    /// Window.TextInput 이벤트가 주는 Latin 문자를 자모(U+3131–U+3163)로 변환한다.
    /// Shift 조합은 Window.TextInput 단계에서 이미 대문자로 반영되므로
    /// 소문자(일반)·대문자(Shift) 모두 한 테이블에 넣는다.
    /// </summary>
    public static class KoreanKeymap
    {
        // ── 두벌식 표준 배열 ──────────────────────────────────────────────────
        //
        //  일반:  q=ㅂ w=ㅈ e=ㄷ r=ㄱ t=ㅅ  y=ㅛ u=ㅕ i=ㅑ o=ㅐ p=ㅔ
        //         a=ㅁ s=ㄴ d=ㅇ f=ㄹ g=ㅎ  h=ㅗ j=ㅓ k=ㅏ l=ㅣ
        //         z=ㅋ x=ㅌ c=ㅊ v=ㅍ b=ㅠ  n=ㅜ m=ㅡ
        //
        //  Shift: Q=ㅃ W=ㅉ E=ㄸ R=ㄲ T=ㅆ  O=ㅒ P=ㅖ
        //         (나머지 Shift는 일반과 동일한 자모)

        private static readonly Dictionary<char, char> s_map = new()
        {
            // ── 자음 (일반) ───────────────────────────────────────────────────
            { 'q', 'ㅂ' }, { 'w', 'ㅈ' }, { 'e', 'ㄷ' }, { 'r', 'ㄱ' }, { 't', 'ㅅ' },
            { 'a', 'ㅁ' }, { 's', 'ㄴ' }, { 'd', 'ㅇ' }, { 'f', 'ㄹ' }, { 'g', 'ㅎ' },
            { 'z', 'ㅋ' }, { 'x', 'ㅌ' }, { 'c', 'ㅊ' }, { 'v', 'ㅍ' },

            // ── 자음 (Shift = 된소리) ─────────────────────────────────────────
            { 'Q', 'ㅃ' }, { 'W', 'ㅉ' }, { 'E', 'ㄸ' }, { 'R', 'ㄲ' }, { 'T', 'ㅆ' },

            // ── 모음 (일반) ───────────────────────────────────────────────────
            { 'y', 'ㅛ' }, { 'u', 'ㅕ' }, { 'i', 'ㅑ' }, { 'o', 'ㅐ' }, { 'p', 'ㅔ' },
            { 'h', 'ㅗ' }, { 'j', 'ㅓ' }, { 'k', 'ㅏ' }, { 'l', 'ㅣ' },
            { 'b', 'ㅠ' }, { 'n', 'ㅜ' }, { 'm', 'ㅡ' },

            // ── 모음 (Shift) ──────────────────────────────────────────────────
            { 'O', 'ㅒ' }, { 'P', 'ㅖ' },

            // Shift+모음 중 일반과 동일한 것도 명시 (누락 방지)
            { 'Y', 'ㅛ' }, { 'U', 'ㅕ' }, { 'I', 'ㅑ' },
            { 'H', 'ㅗ' }, { 'J', 'ㅓ' }, { 'K', 'ㅏ' }, { 'L', 'ㅣ' },
            { 'B', 'ㅠ' }, { 'N', 'ㅜ' }, { 'M', 'ㅡ' },
            { 'A', 'ㅁ' }, { 'S', 'ㄴ' }, { 'D', 'ㅇ' }, { 'F', 'ㄹ' }, { 'G', 'ㅎ' },
            { 'Z', 'ㅋ' }, { 'X', 'ㅌ' }, { 'C', 'ㅊ' }, { 'V', 'ㅍ' },
        };

        /// <summary>
        /// Latin 문자를 두벌식 자모로 변환한다.
        /// </summary>
        /// <param name="latin">Window.TextInput 이벤트에서 받은 문자.</param>
        /// <param name="jamo">변환된 자모 문자.</param>
        /// <returns>변환 성공 여부. false면 <paramref name="latin"/>은 자판 범위 밖.</returns>
        public static bool TryMap(char latin, out char jamo)
            => s_map.TryGetValue(latin, out jamo);
    }
}
